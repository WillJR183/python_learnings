# str - string: são textos (cadeias de caracteres) que estão dentro de aspas. 
# OBS: a linguagem Python é case sensitive, ou seja, palavras ou letras maiúsculas
# são diferentes de minúsculas. Dessa forma, print != Print != PRINT
# OBS: O Python possui tipagem dinâmica, ou seja, detecta automaticamente o tipo de dado.
# OBS: Tudo em Python é objeto (object), incluindo os tipos de dados primitivos

print('Isso é uma string.')  # texto (str)
print("Isso também é uma string")  # texto (str)
print('123')  # text (str)
print(123)  # valor inteiro (int)
print('Texto com "trecho entre aspas".')  # string entre aspas duplas
print("Texto com 'trecho entre aspas'.")  # string entre aspas simples
print('Texto com \'trecho entre aspas\'.')  # string entre aspas simples com caractere de escape
print("Texto com \"trecho entre aspas\".")  # string entre aspas duplas com caractere de escape
print(r'Texto aleatório \n de exemplo.')  # r: read (ignora os elementos reservados e só exibe a string)
