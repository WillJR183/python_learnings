"""
Operadores Relacionais (comparação)

Servem para comparar expressões, ou seja, retornam sempre um valor Booleano.

Igual a ( == ) 
Maior que: ( > )
Maior ou Igual a: ( >= )
Menor que:  <  ( < )
Menor ou Igual a: ( <= )
Diferente de: ( != )
"""

num_1 = 7
num_2 = 10

print(num_1 == num_2)
print(num_1 > num_2)
print(num_1 >= num_2)
print(num_1 < num_2)
print(num_1 <= num_2)
print(num_1 != num_2)
